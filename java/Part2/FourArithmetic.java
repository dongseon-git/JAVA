package ds.java.Part2;

public class FourArithmetic {
    public static void main(String[] args) {
        int a = 12;
        int b = 2;
        int sum = a + b;
        int min = a - b;
        int mul = a * b;
        int div = a / b;

        System.out.println(sum);
        System.out.println(min);
        System.out.println(mul);
        System.out.println(div);
    }
}
