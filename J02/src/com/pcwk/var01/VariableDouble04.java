package com.pcwk.var01;

public class VariableDouble04 {
	/*
	 * 실수 자료형: float, double 부동 소수점 방식: 실수를 지수부와 가수부로 표현(무한의 실수를 표현하기 위한 방식)
	 */
	public static void main(String[] args) {
// float fNum = 3.14; 
// 실수의 default 자료형은 double,
// float 으로 표현하려면 f,F 숫자 끝에 붙인다.
		float fNum = 3.14f;
		double dNum = 3.14;
		System.out.println("fNum=" + fNum);
		System.out.println("dNum=" + dNum);
	}

}
